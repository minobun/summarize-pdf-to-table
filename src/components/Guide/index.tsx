import { ReactElement } from "react";

export default function Guide(): ReactElement {
  return (
    <p style={{ whiteSpace: "pre-line" }}>
      {`
# 使い方ガイド
## アプリケーションの概要
このアプリケーションは、指定されたPDFからテーブル情報を抽出し、表示・編集することができます。利用者は見出し情報を編集して再度抽出し、目的に合わせたテーブルデータを確認することが可能です。

## アプリケーションの利用フロー

1. PDFのURLを入力
利用者は、指定されたテキストボックスにPDFのURLを入力してください。

2. 「PDF抽出」ボタンを押す
入力後、「PDF抽出」ボタンをクリックしてください。システムは指定されたPDFをもとにテーブル情報を自動的に抽出し、画面に表示します。

3. 抽出されたテーブル情報を確認
表示されたテーブルを確認します。必要に応じてテーブル内容の確認や操作が可能です。

4. テーブル情報の見出しを編集（必要な場合）
抽出されたテーブルの見出しを編集したい場合は、テーブルの見出し部分をクリックし、任意の内容に編集してください。

5. 「PDF再抽出」ボタンを押す
見出しの編集後、「PDF再抽出」ボタンを押してください。システムは再度PDFを読み込み、編集された見出し情報に基づいてテーブル情報を再表示します。

## 注意事項
正しいPDFのURLを入力してください。URLが間違っている場合、抽出が正しく行われない可能性があります。
見出しを編集した場合、再抽出しない限り編集内容は反映されません。
`}
    </p>
  );
}
